# Exponents


def main():
    # Exponents
    print(2**2)

    # Modulo: X % Y = remainder given dividing X by Y
    print(111 % 7)

    # Integer division is division that forces an int
    print(10//3)

    # Range(x,y) - is inclusive for x but not for y.
    # range(x) - range from 0-x
    # range(x,y,z) - range with a step of z (-1 => step backwards, 2 = skip 2 each time).
    range_list = [x for x in range(0,5)]
    print(range_list)


main()
